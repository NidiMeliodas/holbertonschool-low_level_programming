/**
 * _strcat - links two strings
 *
 * @dest:
 *
 * @src: 
 *
 * Return: pointer string dest
 */

char *_strcat(char *dest, char *src)
{
	int i = 0;
	int j = 0;
	char desti = (*(dest + i));
	char srcj = (*(src + j));

	while (desti)
	{
		i++;
	}

	while (desti  = srcj)
	{
		i++;
		j++;
	}

	return (dest);
}
